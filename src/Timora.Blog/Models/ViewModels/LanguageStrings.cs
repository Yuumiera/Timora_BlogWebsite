        public static string Get(string key, string culture = "tr-TR")
        {
            if (string.IsNullOrWhiteSpace(culture) || culture == "tr")
                culture = "tr-TR";

            if (Translations.TryGetValue(culture, out var cultureDict) && cultureDict.TryGetValue(key, out var value))
                return value;

            if (Translations.TryGetValue("tr-TR", out var fallbackDict) && fallbackDict.TryGetValue(key, out var fallbackValue))
                return fallbackValue;

            return key;
        }

